# -- source map -------------------------------------------------------


def test_source_map_records_each_test(project):
    _write(
        project / "models/m.yml",
        """\
version: 2
models:
  - name: m
    meta:
      lighttest:
        unit_tests:
          - name: alpha
            given: {u: [{x: 1}]}
            expect: []
""",
    )
    result = compile_project(project)
    smap_data = json.loads(result.source_map_path.read_text())

    entry = smap_data["lighttest__m__alpha"]
    assert entry["source_file"] == "models/m.yml"
    # "- name: alpha" is line 7 (1-indexed) in the YAML above.
    assert entry["source_line"] == 7
    assert entry["model"] == "m"
    assert entry["original_name"] == "alpha"
    assert entry["generated_file"].endswith(f"m{GENERATED_FILE_SUFFIX}")


