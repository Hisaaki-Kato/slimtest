# -- helpers ----------------------------------------------------------


def _write(path: Path, content: str) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(textwrap.dedent(content).lstrip(), encoding="utf-8")
    return path


def _load_yml(path: Path):
    return YAML(typ="safe").load(path.read_text(encoding="utf-8"))


@pytest.fixture
def project(tmp_path: Path):
    """A minimal dbt project skeleton under tmp_path.

    Each fixture-using test seeds factories and model ymls as needed.
    """
    (tmp_path / "models").mkdir()
    (tmp_path / "tests" / "slimtest_factories").mkdir(parents=True)
    return tmp_path


# -- happy path -------------------------------------------------------


def test_empty_project_compiles_to_zero_tests(project):
    result = compile_project(project)
    assert result.test_count == 0
    assert result.generated_files == []
    assert result.source_map_path.exists()
    assert json.loads(result.source_map_path.read_text()) == {}


def test_factory_backed_test_is_expanded(project):
    _write(
        project / "tests/slimtest_factories/users.yml",
        """
        factories:
          user:
            base: {id: 1, name: alice, tier: std}
            traits:
              premium: {tier: premium}
        """,
    )
    _write(
        project / "models/marts/m.yml",
        """
        version: 2
        models:
          - name: m
            meta:
              slimtest:
                unit_tests:
                  - name: test_xxx
                    given:
                      users:
                        - {factory: user, trait: premium, override: {id: 99}}
                    expect:
                      - {a: 1}
        """,
    )

    result = compile_project(project)
    assert result.test_count == 1
    assert len(result.generated_files) == 1

    generated = _load_yml(result.generated_files[0])
    assert generated["version"] == 2
    unit_tests = generated["unit_tests"]
    assert len(unit_tests) == 1
    test = unit_tests[0]
    assert test["name"] == "slimtest__m__test_xxx"
    assert test["model"] == "m"
    assert test["given"] == [
        {
            "input": "ref('users')",
            "rows": [{"id": 99, "name": "alice", "tier": "premium"}],
        }
    ]
    assert test["expect"] == {"rows": [{"a": 1}]}


