# -- upstream auto-fill ----------------------------------------------


def _write_manifest_with_depends_on(project: Path, depends_on: dict[str, list[str]]):
    import json

    nodes = {}
    for model_name, deps in depends_on.items():
        nodes[f"model.proj.{model_name}"] = {
            "resource_type": "model",
            "name": model_name,
            "depends_on": {"nodes": deps},
        }
    target = project / "target"
    target.mkdir(parents=True, exist_ok=True)
    (target / "manifest.json").write_text(
        json.dumps({"nodes": nodes, "sources": {}}), encoding="utf-8"
    )


def test_auto_fill_injects_missing_upstream(project):
    _write(
        project / "tests/slimtest_factories/users.yml",
        """
        factories:
          users:
            base: {id: 1, name: alice}
        """,
    )
    _write(
        project / "models/m.yml",
        """
        models:
          - name: m
            meta:
              slimtest:
                unit_tests:
                  - name: t
                    given:
                      orders: [{x: 1}]
                    expect: []
        """,
    )
    _write_manifest_with_depends_on(
        project, {"m": ["model.proj.users", "model.proj.orders"]}
    )
    result = compile_project(project)
    assert result.test_count == 1
    # Auto-fill is an INFO-level notice, not a warning.
    assert any("users" in n for n in result.notices)
    assert not any("users" in w for w in result.warnings)


