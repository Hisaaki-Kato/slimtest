# -- duplicate detection ---------------------------------------------


def test_duplicate_prefixed_name_across_files_raises(project):
    # Both files put a `test_same` under model `m` -- when prefixed they collide.
    _write(
        project / "models/a.yml",
        """
        models:
          - name: m
            meta:
              lighttest:
                unit_tests:
                  - {name: test_same, given: {u: [{x: 1}]}, expect: []}
        """,
    )
    _write(
        project / "models/b.yml",
        """
        models:
          - name: m
            meta:
              lighttest:
                unit_tests:
                  - {name: test_same, given: {u: [{x: 2}]}, expect: []}
        """,
    )
    with pytest.raises(DuplicateTestNameError) as exc_info:
        compile_project(project)
    assert exc_info.value.name == "lighttest__m__test_same"


